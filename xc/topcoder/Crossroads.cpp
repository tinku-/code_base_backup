class Crossroads {
public:
	vector <int> getOut(vector <int>);
};

vector <int> Crossroads::getOut(vector <int> angles)
{
	vector <int> A;
	for(int i=0;i<angles.size();i++)
	A.push_back(1);
	for(int i=0;i<angles.size();i++)
	{
		for(int j=0;j<angles.size();j++)
		{
			if(i==j)
			continue;
			if((j<i)&&(angles[j]>=angles[i]))
			continue;
			if((j>i)&&(angles[j]<=angles[i]))
			continue;
			int di,dj;
			di=max(90-angles[i],angles[i]-90);
			dj=max(90-angles[j],angles[j]-90);
			if(di<dj)
			A[j]=0;
			else if(dj<di)
			A[i]=0;
			else
			A[max(i,j)]=0;
		}
	}
	vector <int> ans;
	for(int i=0;i<A.size();i++)
	{
		if(A[i]==1)
		ans.push_back(i);
	}
	return ans;
}


