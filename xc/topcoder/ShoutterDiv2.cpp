class ShoutterDiv2 {
public:
	int count(vector <int>, vector <int>);
};

int ShoutterDiv2::count(vector <int> s, vector <int> t)
{
	int ans=0;
	for(int i=0;i<s.size();i++)
	{
		for(int j=0;j<s.size();j++)
		{
			if(i==j)
			continue;
			if((s[i]<=t[j])&&(s[j]<=t[i]))
			ans++;
		}
	}
	ans/=2;
	return ans;
}


