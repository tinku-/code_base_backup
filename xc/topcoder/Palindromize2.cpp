class Palindromize2 {
public:
	string minChanges(string);
};

string Palindromize2::minChanges(string s)
{
	for(int i=0;i<s.size()/2;i++)
	{
		if(s[i]==s[s.size()-1-i])
		continue;
		else
		{
			char ch=min(s[i],s[s.size()-1-i]);
			s[i]=ch;
			s[s.size()-1-i]=ch;
		}
	}
	return s;
}


double test0() {
	string p0 = "ameba";
	Palindromize2 * obj = new Palindromize2();
	clock_t start = clock();
	string my_answer = obj->minChanges(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "abeba";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
