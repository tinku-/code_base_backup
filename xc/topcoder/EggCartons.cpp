	int p0 = 20;
	EggCartons * obj = new EggCartons();
	clock_t start = clock();
	int my_answer = obj->minCartons(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
