class EggCartons {
public:
	int minCartons(int);
};

int EggCartons::minCartons(int n)
{
	if(n%2==1)
	return -1;
	for(int i=n/8;i>=0;i--)
	{
		int x=n-(8*i);
		if(x%6==0)
		return (i+(x/6));
	}
	return -1;
}


