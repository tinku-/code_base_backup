class DifferentStrings {
public:
	int minimize(string, string);
};

int DifferentStrings::minimize(string A, string B)
{
	int ans=10000000,an,bn;
	an=A.size();
	bn=B.size();
	for(int i=0;i<=(bn-an);i++)
	{
		int x=0;
		for(int j=0;j<an;j++)
		{
			if(A[j]!=B[i+j])
			x++;
		}
		//cout<<i<<" "<<x<<"\n";
		ans=min(x,ans);
	}
	return ans;
}


double test0() {
	string p0 = "koder";
	string p1 = "topcoder";
	DifferentStrings * obj = new DifferentStrings();
	clock_t start = clock();
	int my_answer = obj->minimize(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
