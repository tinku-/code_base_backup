class SemiPerfectSquare {
public:
	string check(int);
};

string SemiPerfectSquare::check(int N)
{
	vector <int> factor(1,1);
	int i=2;
	while(N!=1)
	{
		while(N%i==0)
		{
			factor.push_back(i);
			N/=i;
		}
		i++;
	}
	vector <int> counter(1001,0);
	for(i=0;i<factor.size();i++)
	{
		counter[factor[i]]++;
	}
	int a=1,b=1;
	for(i=1;i<=1000;i++)
	{
		if(counter[i]!=0)
		{
			if(counter[i]>1)
			{
				b*=((counter[i]/2)*i);
				a*=((counter[i]-(2*(counter[i]/2)))*i);
			}
			else
			a*=i;
		}
	}
	if(b>a)
	return "Yes";
	else
	return "No";
}


