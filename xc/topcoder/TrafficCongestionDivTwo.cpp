class TrafficCongestionDivTwo {
public:
	long long theMinCars(int);
};

long long TrafficCongestionDivTwo::theMinCars(int treeHeight)
{
	long long int ans[61];
	ans[0]=1;
	ans[1]=1;
	for(int i=2;i<=60;i++)
	{
		ans[i]=1;
		for(int j=0;j<i-1;j++)
		ans[i]+=(2*ans[j]);
	}
	return ans[treeHeight];
}


