class DeerInZooDivTwo {
public:
	vector <int> getminmax(int, int);
};

vector <int> DeerInZooDivTwo::getminmax(int N, int K)
{
	int x=max(0,N-K);
	int y=N-(K/2);
	if(K%2==1)
	y--;
	vector <int> ans;
	ans.push_back(x);
	ans.push_back(y);
	return ans;
}


