class ColorTheCells {
public:
	int minimalTime(vector <int>);
};

int ColorTheCells::minimalTime(vector <int> A)
{
	int ans=0,n=A.size();
	for(int i=1;i<n;i++)
	{
		ans+=2;
	}
	ans+=A[n-2];
	ans+=1;
	return ans;
}


