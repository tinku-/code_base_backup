class CucumberMarket {
public:
	string check(vector <int>, int, int);
};

string CucumberMarket::check(vector <int> price, int budget, int k)
{
	sort(price.begin(),price.end(),greater<int>());
	int sum=0;
	for(int i=0;i<k;i++)
	sum+=price[i];
	if(sum>budget)
	return "NO";
	else
	return "YES";
}


