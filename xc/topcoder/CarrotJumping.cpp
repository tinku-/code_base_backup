class CarrotJumping {
public:
	int theJump(int);
};

int CarrotJumping::theJump(int init)
{
	long long remain1=init,remain2=init,count=0;
	//cout<<remain1<<" "<<remain2<<"\n";
	while(count<100000)
	{
		remain1=(4*remain1)+3;
		remain2=(8*remain1)+7;
		remain1%=1000000007;
		remain2%=1000000007;
		count++;
		//cout<<remain1<<" "<<remain2<<"\n";
		if((remain1==0)||(remain2==0))
		return count;
	}
	return -1;
}

double test0() {
	int p0 = 125000000;
	CarrotJumping * obj = new CarrotJumping();
	clock_t start = clock();
	int my_answer = obj->theJump(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
